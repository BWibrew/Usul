    /**
     * Store a new Site model and populate fields using the API response.
     *
     * @param string $url
     */
    public function populateFromApi(string $url)
    {
        $wp = new Wordpress;
        $site = new Site;

        $site->url = $url;
        $site->root_uri = $wp->discover($url);
        $site->name = $wp->siteName($site->root_uri);
        $site->save();
    }
